    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String aadhar = jTextField1.getText();
        String name = jTextField2.getText();
        String contact = jTextField3.getText();
        jDateChooser1.setDateFormatString("dd/MM/yyyy");
        String dob=((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();
        String age = jTextField4.getText();
        String gender = jTextField5.getText();
        String blood = jTextField6.getText();
        String addr = jTextField7.getText();
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("update recipient set name='"+name+"',contactNo='"+contact+"',dob='"+dob+"',age='"+age+"',gender='"+gender+"',bloodGroup='"+blood+"',address='"+addr+"' where aadharNo='"+aadhar+"'");
            JOptionPane.showMessageDialog(null,"Updated Successfully!!");
            setVisible(false);
            new updateRecipient().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Incorrect data format");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

