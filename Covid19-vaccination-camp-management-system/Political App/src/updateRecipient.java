    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String aadhar = jTextField1.getText();
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from recipient where aadharNo='"+aadhar+"'");
            if(!rs.next()){
                jLabel9.setVisible(true);
            }
            else{
                jLabel9.setVisible(false);
                jTextField1.setEditable(false);
                flag=true;
            }
            if(flag){
                rs = st.executeQuery("select name,contactNo,dob,age,gender,bloodGroup,address from recipient where aadharNo='"+aadhar+"'");
                while(rs.next()){
                    String name=rs.getString("name");
                    String cont=rs.getString("contactNo");
                    String dob=rs.getString("dob");
                    String age=rs.getString("age");
                    String gender=rs.getString("gender");
                    String blood=rs.getString("bloodGroup");
                    String addr=rs.getString("address");
                    jTextField2.setText(name);
                    jTextField3.setText(cont);
                    jTextField4.setText(age);
                    jTextField5.setText(gender);
                    jTextField6.setText(blood);
                    jTextField7.setText(addr);
                    SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
                    java.util.Date d = format.parse(dob);
                    jDateChooser1.setDate(d);
                }
                jTextField2.setVisible(true);
                jTextField3.setVisible(true);
                jTextField4.setVisible(true);
                jTextField5.setVisible(true);
                jTextField6.setVisible(true);
                jTextField7.setVisible(true);
                jDateChooser1.setVisible(true);
                jLabel2.setVisible(true);
                jLabel3.setVisible(true);
                jLabel4.setVisible(true);
                jLabel5.setVisible(true);
                jLabel6.setVisible(true);
                jLabel7.setVisible(true);
                jLabel8.setVisible(true);
                jButton2.setVisible(true);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Connection Falied!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

